    {
        $payment=new Payment;

        $payment->user_id=$request->user_id;
        $payment->payment_mobile_number=$request->payment_mobile_number;
        $payment->transation_number=$request->transation_number;
        $payment->bkash_or_nogod=$request->bkash_or_nogod;
        $payment->payment_date=Carbon::now();
        $payment->save();
        return redirect()->back();

    }

